type Strings = typeof I18N.fr;

function place(p?: { city?: string; country?: string }): string {
  if (!p) return '';
  return [p.city, p.country].filter(Boolean).join(', ');
}

function bornLabel(p: Person, s: Strings): string {
  return p.gender === 'male' ? s.bornOnM : p.gender === 'female' ? s.bornOnF : s.bornOnN;
}
function diedLabel(p: Person, s: Strings): string {
  return p.gender === 'male' ? s.diedM : p.gender === 'female' ? s.diedF : s.diedN;
}

/** Groups persons by generation depth (0-based), each sorted by name. */
function groupByGeneration(tree: FamilyTree): { gen: number; people: Person[] }[] {
  const memo = new Map<string, number>();
  const byGen = new Map<number, Person[]>();
  tree.persons.forEach((p) => {
    const g = getGeneration(p.id, tree.relationships, tree.persons, memo);
    const arr = byGen.get(g) ?? [];
    arr.push(p);
    byGen.set(g, arr);
  });
  return [...byGen.entries()]
    .sort((a, b) => a[0] - b[0])
    .map(([gen, people]) => ({
      gen,
      people: [...people].sort(
        (a, b) => a.lastName.localeCompare(b.lastName) || a.firstName.localeCompare(b.firstName),
      ),
    }));
}

/** Earliest birth year and latest birth/death year across the tree. */
function yearRange(tree: FamilyTree): { min?: number; max?: number } {
  const years: number[] = [];
  tree.persons.forEach((p) => {
    if (p.birthDate) years.push(new Date(p.birthDate).getFullYear());
    if (p.deathDate) years.push(new Date(p.deathDate).getFullYear());
  });
  if (years.length === 0) return {};
  return { min: Math.min(...years), max: Math.max(...years) };
}

// ---------------------------------------------------------------------------
// Page count estimate (cover + sommaire + per-generation pages + index)
// ---------------------------------------------------------------------------

export function estimatePageCount(tree: FamilyTree, options: ExportOptions): number {
  const groups = groupByGeneration(tree);
  // Fiches per page depends on how dense each fiche is.
  const perPage = options.includeBios || options.includePhotos ? 4 : 6;
  let genPages = 0;
  groups.forEach(({ people }) => {
    genPages += Math.max(1, Math.ceil(people.length / perPage));
  });
  const indexPages = Math.max(1, Math.ceil(tree.persons.length / 40));
  // cover (1) + sommaire (1) + generation pages + index
  return 2 + genPages + indexPages;
}

