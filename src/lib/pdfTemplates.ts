// ---------------------------------------------------------------------------
// Per-person fiche
// ---------------------------------------------------------------------------

function fichePerson(
  p: Person,
  tree: FamilyTree,
  options: ExportOptions,
  s: Strings,
  pal: Palette,
): string {
  const { relationships, persons } = tree;
  const parents = getParents(p.id, relationships, persons);
  const spouses = getSpouses(p.id, relationships, persons);
  const children = getChildren(p.id, relationships, persons);

  // NB: this <img> lives inside an HTML string (not JSX), so the Next no-img-element
  // rule does not apply — the booklet is rendered by the browser print window.
  const photo =
    options.includePhotos && p.profilePhoto
      ? `<img class="fiche-photo" src="${escapeHtml(p.profilePhoto)}" alt="">`
      : '';

  // Birth line
  let birthLine = '';
  if (p.birthDate || p.birthPlace?.city) {
    const dateTxt = p.birthDate ? `${bornLabel(p, s)} ${escapeHtml(formatDate(p.birthDate, p.birthDateApprox))}` : '';
    const placeTxt = p.birthPlace?.city ? ` ${s.in} ${escapeHtml(place(p.birthPlace))}` : '';
    birthLine = `<div class="fiche-line">${dateTxt}${placeTxt}</div>`;
  }

  // Death line
  let deathLine = '';
  if (!p.isAlive && (p.deathDate || p.deathPlace?.city)) {
    const dateTxt = p.deathDate ? `${diedLabel(p, s)} ${escapeHtml(formatDate(p.deathDate, p.deathDateApprox))}` : diedLabel(p, s);
    const placeTxt = p.deathPlace?.city ? ` ${s.in} ${escapeHtml(place(p.deathPlace))}` : '';
    deathLine = `<div class="fiche-line"><span class="dagger">&dagger;</span> ${dateTxt}${placeTxt}</div>`;
  }

  const occ = p.occupation
    ? `<div class="fiche-occ"><span class="lbl">${escapeHtml(s.occupation)}</span>${escapeHtml(p.occupation)}</div>`
    : '';

  const bio =
    options.includeBios && p.bio && p.bio.trim()
      ? `<div class="fiche-bio"><span class="lbl">${escapeHtml(s.biography)}</span>${escapeHtml(p.bio)}</div>`
      : '';

  const relLine = (label: string, people: Person[]): string => {
    if (people.length === 0) return '';
    const names = people.map((x) => escapeHtml(getDisplayName(x))).join(', ');
    return `<div><span class="lbl">${escapeHtml(label)} :</span> ${names}</div>`;
  };
  const relsInner = [
    relLine(s.parents, parents),
    relLine(s.spouses, spouses),
    relLine(s.children, children),
  ].join('');
  const rels = relsInner ? `<div class="fiche-rels">${relsInner}</div>` : '';

  let chrono = '';
  if (options.includeTimeline && p.events && p.events.length > 0) {
    const items = p.events
      .map((e) => {
        const y = e.date ? formatYear(e.date) : '';
        return escapeHtml(`${e.type}${y ? ` (${y})` : ''}`);
      })
      .join(' · ');
    if (items) {
      chrono = `<div class="fiche-chrono"><span class="lbl">${escapeHtml(s.chronology)} :</span>${items}</div>`;
    }
  }

  const dagger = !p.isAlive ? ' <span class="dagger">&dagger;</span>' : '';

  return `
    <div class="fiche">
      <div class="fiche-head">
        ${photo}
        <div class="fiche-main">
          <div class="fiche-name">${escapeHtml(getDisplayName(p).toUpperCase())}${dagger}</div>
          ${birthLine}
          ${deathLine}
          ${occ}
        </div>
      </div>
      ${bio}
      ${rels}
      ${chrono}
    </div>`;
}
